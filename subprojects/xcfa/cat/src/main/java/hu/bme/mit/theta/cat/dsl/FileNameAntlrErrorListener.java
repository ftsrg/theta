package hu.bme.mit.theta.cat.dsl;

import org.antlr.v4.runtime.BaseErrorListener;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;

class FileNameAntlrErrorListener extends BaseErrorListener {
    private final String filename;

    FileNameAntlrErrorListener(final String filename) {
        this.filename = filename;
    }

    @Override
    public void syntaxError(Recognizer<?, ?> recognizer, Object offendingSymbol, int line, int charPositionInLine, String msg, RecognitionException e) {
        System.err.println("Syntax error in " + filename + " at " + line + ":" + charPositionInLine + ": " + msg);
    }
}
